    private void btn_FacturaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btn_FacturaMouseClicked
        selectorFactura selector=new selectorFactura(null, true);
        selector.setVisible(true);
        String[] datos=selector.getDatos();
        if(datos!=null)
        {
            lbl_Correlativo.setText(datos[1]);
            txt_Nombre.setText(datos[2]);
            txt_Apellido.setText(datos[3]);
            txt_Nit.setText(datos[4]);
            txt_Nombre4.setText(datos[5]);
            lbl_Fecha.setText(datos[7]);
            btn_Sucursal.setText(datos[8].toUpperCase()+" SERIE: "+datos[6]);
            try {
                DefaultTableModel detalle=Conexion_DB.obtenerDetalleFactura(Integer.parseInt(datos[0]));
                if(detalle!=null)
                    tabla_detalle.setModel(detalle);
            } catch (SQLException|NoSePuedeConectar ex) {
                Logger.getLogger(consultaFactura.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_btn_FacturaMouseClicked

