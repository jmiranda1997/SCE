    private String [] trab; private boolean Seleccionada = false;
    private void btn_AceptarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btn_AceptarMouseClicked
        int Seleccion = tabla_Trabajador.getSelectedRow();
        if (Seleccion != -1) {
            trab = new String[]{tabla_Trabajador.getValueAt(Seleccion, 0).toString(), tabla_Trabajador.getValueAt(Seleccion, 1).toString()};
            Seleccionada = true;
            this.setVisible(false);
        }
    }//GEN-LAST:event_btn_AceptarMouseClicked

